#include <iostream>

using std::cout;
using std::cin;
using std::endl;

int main(){

    unsigned int array_size = 10;

    //Heap Allocation
    int* p = new int [array_size];

    for (int i = 0; i < array_size; i++)
    {
        p[i] = i;
    }

    for (int i = 0; i < array_size; i++)
    {
        cout << p[i] << endl;
    }

    //Heap De-Allocation
    delete[] p;


    
    
    return 0;
}