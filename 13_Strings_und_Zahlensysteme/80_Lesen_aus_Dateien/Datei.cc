#include <iostream>
#include <string>
#include <fstream>

int main()
{
	std::string str;
	std::string text;

	std::ifstream iffile;
	iffile.open("Text.txt");

	if (iffile.is_open())
	{
		while (std::getline(iffile, str))
		{
			text += str + "\n";
		}

		std::cout << text << std::endl;
	}

	iffile.close();

	getchar();
	return 0;
}